def area(a, h): 
    '''принимает сторону и высоту, противолежащую ей, треугольника и возвращает площадь

            4, 5 ==> 10

    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''принимает 3 стороны треугольника и возвращает его периметр
    
            4, 5 , 6 ==> 15
    
    '''
    return a + b + c 