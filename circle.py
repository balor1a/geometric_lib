import math


def area(r):
    '''принимает радиус круга возвращает его площадь
    
        3 ==> 28,2743338823
    
    '''

    return math.pi * r * r


def perimeter(r):
    '''принимает радиус круга возвращает его длину окружности
    
        3 ==> 18,8495559215
    
    '''
    return 2 * math.pi * r

